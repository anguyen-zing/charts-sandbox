// STEP 2: turn { people: [...] } into the same `cfg` array shape as your original file
function buildCfg(json) {
    var cfg = [];
    var seen = {};   // avoids adding the same relationship twice (A->B and B->A)
    var famCount = 1;

    // one node per person, same shape as "Ned Stark" / "Catelyn Stark" etc.
    json.people.forEach(function (p) {
        cfg.push({
            type: 'node',
            id: p.id,
            text: p.name.split(' ')[0],
            dataFullName: p.name,
            style: {
                "background-fit": "xy",
                backgroundImage: p.image
            }
        });
    });

    // couples (partner/spouse) get a family node + 2 links, just like n1, n2, n3...
    // everything else (friend, sibling, industry) is just a direct link
    json.people.forEach(function (person) {
        person.relationships.forEach(function (rel) {
            var key = [person.id, rel.person].sort().join('|');
            if (seen[key]) return;
            seen[key] = true;

            if (rel.type === 'partner' || rel.type === 'spouse') {
                var famId = 'n' + (famCount++);
                cfg.push({ type: 'node', id: famId, cls: 'fam', text: ' ', dataFullName: '' });
                cfg.push({ type: 'link', source: famId, target: person.id });
                cfg.push({ type: 'link', source: famId, target: rel.person });
            } else {
                cfg.push({ type: 'link', source: person.id, target: rel.person });
            }
        });
    });

    return cfg;
}

